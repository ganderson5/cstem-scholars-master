<h2>User roles</h2>

<dl>
    <dt>Advisor</dt>
    <dd>
        Advisors make sure the information provided by the student is correct and accept the application for
        further review. Users with the advisor role may also reject the application if the applicant does not meet
        requirements.
        <br>
        <br>
        When a student fills out an application, they are required to provide the name and email of their advisor.
        They will not be able to submit the application if their advisor's information is not in the system. Therefore
        it is important that all advisors from participating departments are added to the system.
    </dd>

    <dt>Reviewer</dt>
    <dd>
        Reviewers fill out a questionnaire based on the content of the application and make a recommendation to
        fund or reject the proposed project. The results are visible only to administrators and never sent to the
        student.
    </dd>

    <dt>Administrator</dt>
    <dd>
        Administrators manage user accounts, application periods, and make the final decision to award or reject an
        application.
    </dd>
</dl>

<h2>Application Workflow</h2>

<ol>
    <li>Student fills out the application.</li>
    <li>Advisor accepts or rejects an application.</li>
    <li>Three random reviewers are assigned to review the accepted application.</li>
    <li>Administrator makes the final decision to award or reject the application.</li>
    <li>
        A follow-up email should be sent separately to each awarded student by the department funding the project with
        further instructions.
    </li>
</ol>

